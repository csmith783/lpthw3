# Common Escape Sequences
# \\ backslash
# \' single quote
# \" double quote
# \a ASCII bell 
# \n line feed
# \r carriage return
# \t tab


tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm a \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
