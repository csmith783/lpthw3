# Rules for if-statements
# Every if statment must have an else
# if the else should never run because it does not make sense, then you must use a 'die' function
## the die function should print out an error message, this will find many errors
# never nest if statements more that two deep and always try to do them one deep
# treat if-else statements like paragraphs
### put blank lines before and after them
# your boolean test should be simple, if they are complex, move their calculations to variables before the function

# Rules for while-loops
# use a while-loop only to loop forever, and that means probably never
# use afor loop for all other kinds of looping, especially if there is afixed or limited # of things to loop over

# Tips for debugging 
# do not use a"debugger" - you willl not get specifically useful information 
# the best way to debug a program is to use print statements to print values of variables at certain points 
# make sure parts of your program work as you work on them. 
# do not write an entire script before running it. 
# Always run your script as you write it. 

